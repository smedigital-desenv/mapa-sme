CREATE OR REPLACE FUNCTION ee_apoio() RETURNS json LANGUAGE sql STABLE SECURITY DEFINER SET search_path=public AS $FN$
  SELECT json_build_object(
    'geral',(SELECT json_build_object('escolas',count(*),'prof_manha',coalesce(sum(prof_manha),0),'prof_tarde',coalesce(sum(prof_tarde),0),'professores',coalesce(sum(prof_manha+prof_tarde),0),'apoios',coalesce(sum(apoios),0)) FROM ee_apoio),
    'escolas',coalesce((SELECT json_agg(json_build_array(tipo,escola,prof_manha,prof_tarde,apoios)) FROM (SELECT * FROM ee_apoio ORDER BY tipo,escola) s),'[]'::json)
  );
$FN$;
CREATE OR REPLACE FUNCTION ee_liminar() RETURNS json LANGUAGE sql STABLE SECURITY DEFINER SET search_path=public AS $FN$
  SELECT json_build_object(
    'geral',(SELECT json_build_object(
       'alunos',(SELECT count(*) FROM ee_liminar_aluno),
       'turmas',(SELECT coalesce(sum(total_turmas),0) FROM ee_liminar_unidade),
       'unidades',(SELECT count(*) FROM ee_liminar_unidade),
       'atendidas',(SELECT count(*) FROM ee_liminar_unidade WHERE status='Atendida'),
       'pendentes',(SELECT count(*) FROM ee_liminar_unidade WHERE status='Pendência'),
       'fora',(SELECT count(*) FROM ee_liminar_unidade WHERE status='Fora da rede municipal'))),
    'unidades',coalesce((SELECT json_agg(json_build_array(escola,tipo,total_turmas,turmas_manha,turmas_tarde,prof_manha,prof_tarde,status)) FROM (SELECT * FROM ee_liminar_unidade ORDER BY status,escola) s),'[]'::json),
    'turmas',coalesce((SELECT json_agg(json_build_array(escola,turma,periodo,alunos,status)) FROM (SELECT * FROM ee_liminar_turma ORDER BY escola,turma) s),'[]'::json),
    'alunos',coalesce((SELECT json_agg(json_build_array(aluno,ra,escola,turma,periodo,deficiencia)) FROM (SELECT * FROM ee_liminar_aluno ORDER BY escola,turma,aluno) s),'[]'::json)
  );
$FN$;
