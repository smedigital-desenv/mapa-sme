ALTER TABLE ee_liminar_turma DISABLE ROW LEVEL SECURITY;
CREATE TABLE ee_liminar_aluno (id serial PRIMARY KEY, aluno text, ra text, escola text, turma text, periodo text, deficiencia text);
ALTER TABLE ee_liminar_aluno DISABLE ROW LEVEL SECURITY;
